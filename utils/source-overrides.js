function normalizeMonth(value) {
  return String(value || '').replace(/^'/, '');
}

function mergeSeries(months, fallbackSeries, sourceMonths, sourceSeries) {
  const sourceMap = new Map(
    sourceMonths.map((month, index) => [normalizeMonth(month), sourceSeries[index]]),
  );

  return months.map((month, index) => {
    const normalized = normalizeMonth(month);
    if (sourceMap.has(normalized)) return sourceMap.get(normalized);
    return fallbackSeries?.[index] ?? null;
  });
}

